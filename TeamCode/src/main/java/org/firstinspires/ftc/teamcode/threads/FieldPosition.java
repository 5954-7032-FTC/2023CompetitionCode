package org.firstinspires.ftc.teamcode.threads;

public class FieldPosition {

    float X,Y,Z;
    float roll,pitch,heading;

    public FieldPosition(float X, float Y, float Z, float roll, float pitch, float heading) {
        this.X = X;
        this.Y = Y;
        this.Z = Z;
        this.roll = roll;
        this.pitch = pitch;
        this.heading = heading;
    }
}
